package com.undercontroll.api.model;

import com.undercontroll.api.model.enums.PasswordEventStatus;
import com.undercontroll.api.model.enums.PasswordEventType;
import jakarta.persistence.*;
import jakarta.validation.constraints.NotNull;
import lombok.*;
import org.hibernate.annotations.CreationTimestamp;

import java.time.LocalDateTime;
import java.util.UUID;

@Getter
@Setter
@NoArgsConstructor
@AllArgsConstructor
@Builder
@Entity
@Table(name = "password_event")
public class PasswordEvent {

    @Id
    private UUID id;

    @Enumerated(EnumType.STRING)
    private PasswordEventType type;

    @Enumerated(EnumType.STRING)
    private PasswordEventStatus status;

    private String value;

    @NotNull
    private String userPhone;

    private String userAgent;

    @CreationTimestamp
    private LocalDateTime createdAt;

}
